public class InnercreditValues {

    String creditcompanyName = null;
    String invoiceno = null;
    String creditinneramount = null;
    String paymentdate = null;

    public String getCreditcompanyName() {
        return creditcompanyName;
    }

    public void setCreditcompanyName(String creditcompanyName) {
        this.creditcompanyName = creditcompanyName;
    }

    public String getInvoiceno() {
        return invoiceno;
    }

    public void setInvoiceno(String invoiceno) {
        this.invoiceno = invoiceno;
    }

    public String getCreditinneramount() {
        return creditinneramount;
    }

    public void setCreditinneramount(String creditinneramount) {
        this.creditinneramount = creditinneramount;
    }

    public String getPaymentdate() {
        return paymentdate;
    }

    public void setPaymentdate(String paymentdate) {
        this.paymentdate = paymentdate;
    }
}
